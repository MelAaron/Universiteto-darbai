package proj;

public class Points {
    String from;
    String to;
    String type;
    int flowTons;
    int flowTonKMs;
    public Points(String from, String to, String type, int flowTons, int flowTonKMs){
        this.from = from;
        this.to = to;
        this.type = type;
        this.flowTons = flowTons;
        this.flowTonKMs = flowTonKMs;
    }
}
